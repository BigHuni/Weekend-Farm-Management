package knu.mypackage;
public class Fruit {
	String name;

	public Fruit(String name) {
		super();
		this.name = name;
	}

	public String toString() {
		return "Fruit [name=" + name + "]";
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}
		
}
