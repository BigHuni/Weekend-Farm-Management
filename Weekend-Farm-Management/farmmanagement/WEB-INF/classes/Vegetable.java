package knu.mypackage;
public class Vegetable {
	String name;

	public Vegetable(String name) {
		super();
		this.name = name;
	}

	public String toString() {
		return "Vegetable [name=" + name + "]";
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}
	
}
