package knu.mypackage;
public class Humidity {
	String name;

	public Humidity(String name) {
		super();
		this.name = name;
	}

	public String toString() {
		return "Humidity [name=" + name + "]";
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}
	
}
